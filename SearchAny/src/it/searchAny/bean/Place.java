package it.searchAny.bean;

public class Place {
	private String citta = null;
	private String nazione = null;
	private String lngCentre = null;
	private String latCentre = null;
	private String lngNorthEast = null;
	private String latNorthEast = null;
	private String lngSouthWest = null;
	private String latSouthWest = null;
	public String getCitta() {
		return citta;
	}
	public void setCitta(String citta) {
		this.citta = citta;
	}
	public String getNazione() {
		return nazione;
	}
	public void setNazione(String nazione) {
		this.nazione = nazione;
	}
	public String getLngCentre() {
		return lngCentre;
	}
	public void setLngCentre(String lngCentre) {
		this.lngCentre = lngCentre;
	}
	public String getLatCentre() {
		return latCentre;
	}
	public void setLatCentre(String latCentre) {
		this.latCentre = latCentre;
	}
	public String getLngNorthEast() {
		return lngNorthEast;
	}
	public void setLngNorthEast(String lngNorthEast) {
		this.lngNorthEast = lngNorthEast;
	}
	public String getLatNorthEast() {
		return latNorthEast;
	}
	public void setLatNorthEast(String latNorthEast) {
		this.latNorthEast = latNorthEast;
	}
	public String getLngSouthWest() {
		return lngSouthWest;
	}
	public void setLngSouthWest(String lngSouthWest) {
		this.lngSouthWest = lngSouthWest;
	}
	public String getLatSouthWest() {
		return latSouthWest;
	}
	public void setLatSouthWest(String latSouthWest) {
		this.latSouthWest = latSouthWest;
	}
}
